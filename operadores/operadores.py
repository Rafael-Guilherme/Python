# > Operações Matemáticas (Aritméticas)

"""
- soma: +
- subtração: -
- multiplucação: *
- divisão: /
- divisão inteira: //
- resto da divisão: %
- potência: **
"""

n1 = 10
n2 = 20

print(n1 + n2)
print(n1 - n2)
print(n1 * n2)
print(n1 / n2)
print(n1 // n2)
print(20 % 3)
print(2 ** 3)

