lista = [1, 3, 12, 8, 2]

print(lista)

# append

lista.append(3)

print(lista)

# insert

lista.insert(2, 10)

print(lista)

# extend

lista2 = [1, 2, 3]

lista.extend(lista2)

print(lista)

# pop

lista.pop()

print(lista)

lista.pop(0)

print(lista)

# remove

lista.remove(3)

print(lista)

# count

print(lista.count(2))

# index 

print(lista.index(12))

# sort 

lista.sort()

print(lista)

